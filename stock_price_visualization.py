# stock_price_visualization.py
# Compare Tesla and Apple stock prices using Python

import pandas as pd
import matplotlib.pyplot as plt

# Read dataset
df = pd.read_csv("stock_data.csv")

# Ensure Date is datetime type
df["Date"] = pd.to_datetime(df["Date"])

# Plot Tesla vs Apple
plt.figure(figsize=(10,6))
plt.plot(df["Date"], df["Tesla"], label="Tesla", color="red")
plt.plot(df["Date"], df["Apple"], label="Apple", color="blue")
plt.title("Tesla vs Apple Stock Prices")
plt.xlabel("Date")
plt.ylabel("Stock Price (USD)")
plt.legend()
plt.tight_layout()
plt.savefig("stock_comparison.png")
plt.show()

# Save cleaned data
df.to_csv("cleaned_stock_data.csv", index=False)

